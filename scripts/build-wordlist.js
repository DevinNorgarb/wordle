#!/usr/bin/env node
/**
 * Reads valid-wordle-words.txt and writes js/valid-guess-words.generated.js
 * Run from repo root: node scripts/build-wordlist.js
 */
const fs = require("fs");
const path = require("path");

const root = path.join(__dirname, "..");
const src = path.join(root, "valid-wordle-words.txt");
const out = path.join(root, "js", "valid-guess-words.generated.js");

const raw = fs.readFileSync(src, "utf8");
const words = raw
    .split(/\r?\n/)
    .map((w) => w.trim().toLowerCase())
    .filter((w) => w.length === 5 && /^[a-z]+$/.test(w));

const unique = [...new Set(words)];
unique.sort();

const body = unique.map((w) => JSON.stringify(w)).join(",\n  ");

const banner = `/* Auto-generated by scripts/build-wordlist.js — do not edit. Source: valid-wordle-words.txt (${unique.length} words) */
`;

fs.writeFileSync(
    out,
    `${banner}const VALID_GUESS_WORD_LIST = [\n  ${body}\n];\n`,
    "utf8"
);

console.log(`Wrote ${unique.length} words to ${path.relative(root, out)}`);
